import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

// Load env
const envContent = fs.readFileSync('.env', 'utf8');
envContent.split('\n').forEach(line => {
  const [k, ...v] = line.split('=');
  if (k && v.length) process.env[k.trim()] = v.join('=').trim();
});

const { getGitHubUser, createRepo, pushDirToRepo } = await import('./src/github.js');
const { fullDeploy } = await import('./src/cloudflare.js');

const githubToken = process.env.GITHUB_TOKEN;
const cfToken = process.env.CF_TOKEN;
const cfAccountId = process.env.CF_ACCOUNT_ID;
const outDir = 'dist/david-brooks';
const repoName = 'david-brooks-archive';

console.log('GitHub token:', githubToken ? '✅' : '❌');
console.log('CF token:', cfToken ? '✅' : '❌');
console.log('CF account:', cfAccountId ? '✅' : '❌');

// Push to GitHub
try {
  const ghUser = await getGitHubUser(githubToken);
  console.log('GitHub user:', ghUser);
  
  let repoUrl;
  try {
    const repo = await createRepo(githubToken, {
      name: repoName,
      description: 'Article archive for David Brooks — generated by PersonArchive',
      isPrivate: false
    });
    repoUrl = repo.html_url;
    console.log('Repo created:', repoUrl);
  } catch(e) {
    if (e.message.includes('422') || e.message.includes('already exists')) {
      repoUrl = `https://github.com/${ghUser}/${repoName}`;
      console.log('Repo exists already:', repoUrl);
    } else {
      throw e;
    }
  }
  
  await pushDirToRepo(githubToken, {
    owner: ghUser,
    repo: repoName,
    localDir: outDir,
    message: 'feat: generate David Brooks archive (70 articles)'
  });
  console.log('GitHub push ✅ —', repoUrl);
  
} catch(e) {
  console.error('GitHub error:', e.message);
}

// Deploy to Cloudflare
try {
  const deployUrl = await fullDeploy(cfToken, cfAccountId, repoName, outDir);
  console.log('✅ Deployed to:', deployUrl);
} catch(e) {
  console.error('CF error:', e.message);
}
